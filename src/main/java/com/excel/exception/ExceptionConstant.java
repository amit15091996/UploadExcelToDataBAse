package com.excel.exception;

public class ExceptionConstant extends RuntimeException{



	public ExceptionConstant() {
		super();

	}

	public ExceptionConstant(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
		super(message, cause, enableSuppression, writableStackTrace);

	}

	public ExceptionConstant(String message, Throwable cause) {
		super(message, cause);

	}

	public ExceptionConstant(String message) {
		super(message);

	}

	public ExceptionConstant(Throwable cause) {
		super(cause);

	}



	
	

}
